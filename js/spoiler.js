$(document).ready(function(){ 
	$('.questions__item-question--1').click(function(){ 
				let krest = $('#arrow__question--1');
				if(krest.hasClass('arrow_down'))
					{
					krest.removeClass('arrow_down');
					krest.addClass('arrow_up');
					$('#question-content--1').slideDown('slow');
					$(".questions__item-question--1 .question-arrow-down").css("display", "none");
     $(".questions__item-question--1 .question-arrow-up").css("display", "block");
					}
			else
					{
					krest.removeClass('arrow_up');
					krest.addClass('arrow_down');
					$('#question-content--1').slideUp('fast');
					$(".questions__item-question--1 .question-arrow-down").css("display", "block");
     $(".questions__item-question--1 .question-arrow-up").css("display", "none");
					}
			})
	});

